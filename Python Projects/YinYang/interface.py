#---------------------===#[ Create Headers For Instructions ]#===--------------------#
#------------------------------------------------------------------------------------#
def makeHeader(name):
    return BARS % name.upper().strip()

#------------------------------------------------------------------------------------#
#--------------------------===#[ pad lines for Output ]#===--------------------------#
#------------------------------------------------------------------------------------#
def padLine(line, size):
    sp1 = ' ' * int((size - len(line)) / 2)
    sp2 = ' ' * (size - (len(sp1) + len(line)))
    ret = sp1 + line + sp2

    return ret

#------------------------------------------------------------------------------------#
#-------------------------===#[ Flush the Input Buffer ]#===-------------------------#
#------------------------------------------------------------------------------------#
def flushInput():
    while kbhit(): getch()
